int superDigit(string n, int k) {

    if(n.size() == 1 && k == 1) return n[0]-'0';

    long long sum = 0;
    for(char c:n)
        sum+=(c-'0');
    
    sum*=k;

    while(sum > 10){
        long long s = 0;
        while(sum > 0){
            s+=sum%10;
            sum/=10;
        }
        sum = s;
    }

    return (int)sum;
}

